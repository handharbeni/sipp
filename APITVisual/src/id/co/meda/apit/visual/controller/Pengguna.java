package id.co.meda.apit.visual.controller;

/**
 *
 * @author PT. Data Bumi Indonesia
 */
public class Pengguna {
    private Integer idUser;
    private String nmUser;
    private String pwd;
    private String nip;
    private String nmLengkap;
    private String kdRole;

    
    
    
    /**
     * @return the nmUser
     */
    public String getNmUser() {
        return nmUser;
    }

    /**
     * @param nmUser the nmUser to set
     */
    public void setNmUser(String nmUser) {
        this.nmUser = nmUser;
    }

    /**
     * @return the pwd
     */
    public String getPwd() {
        return pwd;
    }

    /**
     * @param pwd the pwd to set
     */
    public void setPwd(String pwd) {
        this.pwd = pwd;
    }

    /**
     * @return the nip
     */
    public String getNip() {
        return nip;
    }

    /**
     * @param nip the nip to set
     */
    public void setNip(String nip) {
        this.nip = nip;
    }

    /**
     * @return the nmLengkap
     */
    public String getNmLengkap() {
        return nmLengkap;
    }

    /**
     * @param nmLengkap the nmLengkap to set
     */
    public void setNmLengkap(String nmLengkap) {
        this.nmLengkap = nmLengkap;
    }

    /**
     * @return the kdRole
     */
    public String getKdRole() {
        return kdRole;
    }

    /**
     * @param kdRole the kdRole to set
     */
    public void setKdRole(String kdRole) {
        this.kdRole = kdRole;
    }

    /**
     * @return the idUser
     */
    public Integer getIdUser() {
        return idUser;
    }

    /**
     * @param idUser the idUser to set
     */
    public void setIdUser(Integer idUser) {
        this.idUser = idUser;
    }
    
    
    
}
