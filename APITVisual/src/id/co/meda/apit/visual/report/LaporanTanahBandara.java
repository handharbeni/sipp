package id.co.meda.apit.visual.report;

/**
 *
 * @author PT. Databumi Indonesia
 */
public class LaporanTanahBandara {
    
    private String jnsPenggunaanTanah = "";
    private String luas;
    private String nir;
    private String nilPerM;
    private String nilTotal;

    /**
     * @return the jnsPenggunaanTanah
     */
    public String getJnsPenggunaanTanah() {
        return jnsPenggunaanTanah;
    }

    /**
     * @param jnsPenggunaanTanah the jnsPenggunaanTanah to set
     */
    public void setJnsPenggunaanTanah(String jnsPenggunaanTanah) {
        this.jnsPenggunaanTanah = jnsPenggunaanTanah;
    }

    /**
     * @return the luas
     */
    public String getLuas() {
        return luas;
    }

    /**
     * @param luas the luas to set
     */
    public void setLuas(String luas) {
        this.luas = luas;
    }

    /**
     * @return the nir
     */
    public String getNir() {
        return nir;
    }

    /**
     * @param nir the nir to set
     */
    public void setNir(String nir) {
        this.nir = nir;
    }

    /**
     * @return the nilPerM
     */
    public String getNilPerM() {
        return nilPerM;
    }

    /**
     * @param nilPerM the nilPerM to set
     */
    public void setNilPerM(String nilPerM) {
        this.nilPerM = nilPerM;
    }

    /**
     * @return the nilTotal
     */
    public String getNilTotal() {
        return nilTotal;
    }

    /**
     * @param nilTotal the nilTotal to set
     */
    public void setNilTotal(String nilTotal) {
        this.nilTotal = nilTotal;
    }
    
}
