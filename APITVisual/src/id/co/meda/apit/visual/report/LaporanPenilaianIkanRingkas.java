package id.co.meda.apit.visual.report;

/**
 *
 * @author PT. Databumi Indonesia
 */
public class LaporanPenilaianIkanRingkas {
    
   private String njopTanah;
   private String njopBangunan;
   private String njopPerikanan;
   private String totalNjop;

    /**
     * @return the njopTanah
     */
    public String getNjopTanah() {
        return njopTanah;
    }

    /**
     * @param njopTanah the njopTanah to set
     */
    public void setNjopTanah(String njopTanah) {
        this.njopTanah = njopTanah;
    }

    /**
     * @return the njopBangunan
     */
    public String getNjopBangunan() {
        return njopBangunan;
    }

    /**
     * @param njopBangunan the njopBangunan to set
     */
    public void setNjopBangunan(String njopBangunan) {
        this.njopBangunan = njopBangunan;
    }

    /**
     * @return the njopPerikanan
     */
    public String getNjopPerikanan() {
        return njopPerikanan;
    }

    /**
     * @param njopPerikanan the njopPerikanan to set
     */
    public void setNjopPerikanan(String njopPerikanan) {
        this.njopPerikanan = njopPerikanan;
    }

    /**
     * @return the totalNjop
     */
    public String getTotalNjop() {
        return totalNjop;
    }

    /**
     * @param totalNjop the totalNjop to set
     */
    public void setTotalNjop(String totalNjop) {
        this.totalNjop = totalNjop;
    }
   
    
}
