package id.co.meda.apit.visual.report;

/**
 *
 * @author PT. Databumi Indonesia
 */
public class LaporanCrnPelabuhan {
    
    private String jenisPekerjaan;
    private String satuan;
    private String volume;
    private String hrg_bahan;
    private String hrg_upah;
    private String hrg_jumlah;
    private String hrg_total;

    /**
     * @return the jenisPekerjaan
     */
    public String getJenisPekerjaan() {
        return jenisPekerjaan;
    }

    /**
     * @param jenisPekerjaan the jenisPekerjaan to set
     */
    public void setJenisPekerjaan(String jenisPekerjaan) {
        this.jenisPekerjaan = jenisPekerjaan;
    }

    /**
     * @return the satuan
     */
    public String getSatuan() {
        return satuan;
    }

    /**
     * @param satuan the satuan to set
     */
    public void setSatuan(String satuan) {
        this.satuan = satuan;
    }

    /**
     * @return the volume
     */
    public String getVolume() {
        return volume;
    }

    /**
     * @param volume the volume to set
     */
    public void setVolume(String volume) {
        this.volume = volume;
    }

    /**
     * @return the hrg_bahan
     */
    public String getHrg_bahan() {
        return hrg_bahan;
    }

    /**
     * @param hrg_bahan the hrg_bahan to set
     */
    public void setHrg_bahan(String hrg_bahan) {
        this.hrg_bahan = hrg_bahan;
    }

    /**
     * @return the hrg_upah
     */
    public String getHrg_upah() {
        return hrg_upah;
    }

    /**
     * @param hrg_upah the hrg_upah to set
     */
    public void setHrg_upah(String hrg_upah) {
        this.hrg_upah = hrg_upah;
    }

    /**
     * @return the hrg_jumlah
     */
    public String getHrg_jumlah() {
        return hrg_jumlah;
    }

    /**
     * @param hrg_jumlah the hrg_jumlah to set
     */
    public void setHrg_jumlah(String hrg_jumlah) {
        this.hrg_jumlah = hrg_jumlah;
    }

    /**
     * @return the hrg_total
     */
    public String getHrg_total() {
        return hrg_total;
    }

    /**
     * @param hrg_total the hrg_total to set
     */
    public void setHrg_total(String hrg_total) {
        this.hrg_total = hrg_total;
    }
    
}
