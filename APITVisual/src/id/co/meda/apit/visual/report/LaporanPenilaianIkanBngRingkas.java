public class LaporanPenilaianIkanBngRingkas {
    
    private String luasTotal;
    private String nilaiTotal;    
    private String nilaiPerM2;
    private String kelasBangunan;
    private String njop;

    /**
     * @return the luasTotal
     */
    public String getLuasTotal() {
        return luasTotal;
    }

    /**
     * @param luasTotal the luasTotal to set
     */
    public void setLuasTotal(String luasTotal) {
        this.luasTotal = luasTotal;
    }

    /**
     * @return the nilaiTotal
     */
    public String getNilaiTotal() {
        return nilaiTotal;
    }

    /**
     * @param nilaiTotal the nilaiTotal to set
     */
    public void setNilaiTotal(String nilaiTotal) {
        this.nilaiTotal = nilaiTotal;
    }

    /**
     * @return the nilaiPerM2
     */
    public String getNilaiPerM2() {
        return nilaiPerM2;
    }

    /**
     * @param nilaiPerM2 the nilaiPerM2 to set
     */
    public void setNilaiPerM2(String nilaiPerM2) {
        this.nilaiPerM2 = nilaiPerM2;
    }

    /**
     * @return the kelasBangunan
     */
    public String getKelasBangunan() {
        return kelasBangunan;
    }

    /**
     * @param kelasBangunan the kelasBangunan to set
     */
    public void setKelasBangunan(String kelasBangunan) {
        this.kelasBangunan = kelasBangunan;
    }

    /**
     * @return the njop
     */
    public String getNjop() {
        return njop;
    }

    /**
     * @param njop the njop to set
     */
    public void setNjop(String njop) {
        this.njop = njop;
    }
    
    
}
