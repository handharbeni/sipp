package id.co.meda.apit.visual.report;

/**
 *
 * @author I Made Sugiada
 */
public class LaporanPenilaianDetil {
    private String namaItem;
    private String luasItem;
    private String satuanItem;
    private String namaKomponen;
    private String nilaiKomponen;
    private String nilaiTotal;

    /**
     * @return the namaItem
     */
    public String getNamaItem() {
        return namaItem;
    }

    /**
     * @param namaItem the namaItem to set
     */
    public void setNamaItem(String namaItem) {
        this.namaItem = namaItem;
    }

    /**
     * @return the luasItem
     */
    public String getLuasItem() {
        return luasItem;
    }

    /**
     * @param luasItem the luasItem to set
     */
    public void setLuasItem(String luasItem) {
        this.luasItem = luasItem;
    }

    /**
     * @return the satuanItem
     */
    public String getSatuanItem() {
        return satuanItem;
    }

    /**
     * @param satuanItem the satuanItem to set
     */
    public void setSatuanItem(String satuanItem) {
        this.satuanItem = satuanItem;
    }

    /**
     * @return the namaKomponen
     */
    public String getNamaKomponen() {
        return namaKomponen;
    }

    /**
     * @param namaKomponen the namaKomponen to set
     */
    public void setNamaKomponen(String namaKomponen) {
        this.namaKomponen = namaKomponen;
    }

    /**
     * @return the nilaiKomponen
     */
    public String getNilaiKomponen() {
        return nilaiKomponen;
    }

    /**
     * @param nilaiKomponen the nilaiKomponen to set
     */
    public void setNilaiKomponen(String nilaiKomponen) {
        this.nilaiKomponen = nilaiKomponen;
    }

    /**
     * @return the nilaiTotal
     */
    public String getNilaiTotal() {
        return nilaiTotal;
    }

    /**
     * @param nilaiTotal the nilaiTotal to set
     */
    public void setNilaiTotal(String nilaiTotal) {
        this.nilaiTotal = nilaiTotal;
    }

}
