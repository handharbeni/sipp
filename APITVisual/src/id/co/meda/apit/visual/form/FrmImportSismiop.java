    private void txtNopKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNopKeyPressed
        // TODO add your handling code here:
        if (evt.getKeyCode()==KeyEvent.VK_ENTER) {
            nop = txtNop.getText().replace(".","").replace("-","");
            cariNopSis();
            cariBangunanSis();
            cariNopExis();
            cariBangunanExis();
        }
    }//GEN-LAST:event_txtNopKeyPressed

    private void btImportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btImportActionPerformed
        // TODO add your handling code here:
        if (spopSis.getNop()==null) {
            JOptionPane.showMessageDialog(this, "Anda belum mengambil data dari database SISMIOP ...");
            txtNop.requestFocus();
        } else {
            if (spopExis.getNop()!=null) {
                if (JOptionPane.showConfirmDialog(this, "Data yang akan diimpor sudah ada di database..., "
                        + "\napakah akan ditimpa ?", "Konfirmasi", JOptionPane.YES_NO_OPTION)==JOptionPane.YES_OPTION) {
                    prosesImpor();
                }
            } else {
                prosesImpor();
            }
        }
    }//GEN-LAST:event_btImportActionPerformed
    
    private void prosesImpor() {
        //copy isi Spop Lspop dari sis ke exis
        spopExis = new Spop();
        spopSis.setJumlahBangunan((listLspopSis.size()>0)?listLspopSis.size():0);
        spopExis = spopSis;
        
        listLspopExis = new ArrayList<>();
        for (int i=0;i<listLspopSis.size();i++) {
            listLspopExis.add(listLspopSis.get(i));
        }
        
        //tampilkan di layar exis
        isiLabelExis();
        bentukTbl(tblBngExis, listLspopExis);
        
        String hasil = ctrlImp.simpanDb(spopExis, listLspopExis);
        if (hasil.equals("ok")) {
            JOptionPane.showMessageDialog(this, "Import berhasil ....");
        } else {
            JOptionPane.showMessageDialog(this, "Import gagal !!!");
        }
    }
    
    private void cariNopExis() {
        spopExis = new Spop();
        ToolsPenilaian.ambilSpopFromDb(nop, spopExis);
        
        //ini dapat diartikan, jika nop di kedua database ada
        if (spopSis.getNop()!=null && spopExis.getNop()!=null) {
            isiLabelExis();
        }
    }
    
    private void isiLabelExis() {
        lbNopExis.setText(txtNop.getText());
        lbNmWpExis.setText(spopExis.getNamaWp());
        lbAlmOpExis.setText("<html>"+spopExis.getJalanOp()+" "+((spopExis.getNoJalanOp()==null)?"-":spopExis.getNoJalanOp())
                +", Kelurahan: "+spopExis.getKelurahanOp()+"</html>");
        lbLsBumiExis.setText(numFormat.format(spopExis.getLuasTanah()));
    }
    
    private void cariBangunanExis() {
        listLspopExis = new ArrayList();
        ToolsPenilaian.ambilLspopFromDbPerNop(nop, listLspopExis);
        
        if (spopSis.getNop()!=null && spopExis.getNop()!=null) {
            bentukTbl(tblBngExis, listLspopExis);
        }
    }
    
    private void cariBangunanSis() {
        listLspopSis = new ArrayList();
        String status = ctrlImp.ambilDataLspop(nop, listLspopSis);
        
        if (status.equals("ok")) {
            bentukTbl(tblBngSis, listLspopSis);
        }
    }
    
    private void cariNopSis() {
        
        kosongkan();
        
        spopSis = new Spop();
        String status = ctrlImp.ambilDataSpop(nop,spopSis);
        
        if (status.equals("ok") && spopSis.getNamaWp() != null) {
            lbNopSis.setText(txtNop.getText());
            lbNmWpSis.setText(spopSis.getNamaWp());
            lbAlmOpSis.setText("<html>"+spopSis.getJalanOp()+" "+((spopSis.getNoJalanOp()==null)?"-":spopSis.getNoJalanOp())
                    +", Kelurahan: "+spopSis.getKelurahanOp()+"</html>");
            lbLsBumiSis.setText(numFormat.format(spopSis.getLuasTanah()));
            
        } else {
            JOptionPane.showMessageDialog(this, "NOP tidak ditemukan di basisdata SISMIOP ....");
            txtNop.setText("");
        }
    }
    
    private void bentukTbl(JTable a) {
        a.setModel(ctrlImp.getTableModel());
        aturKolom(a);
    }
    private void bentukTbl(JTable a, ArrayList<LspopNonStandar> l) {
        a.setModel(ctrlImp.getTableModel(l));
        aturKolom(a);
    }
    
    private void aturKolom(JTable a) {
        a.getColumnModel().getColumn(0).setPreferredWidth(4);        
        a.getColumnModel().getColumn(1).setPreferredWidth(160);        
        a.getColumnModel().getColumn(2).setPreferredWidth(25);        
    }
    
    private void kosongkan() {
        lbNopSis.setText("-");      lbNopExis.setText("-");
        lbNmWpSis.setText("-");     lbNmWpExis.setText("-"); 
        lbAlmOpSis.setText("-");     lbAlmOpExis.setText("-"); 
        lbLsBumiSis.setText("-");     lbLsBumiExis.setText("-"); 
        
        bentukTbl(tblBngSis);
        bentukTbl(tblBngExis);
    }
    
    

