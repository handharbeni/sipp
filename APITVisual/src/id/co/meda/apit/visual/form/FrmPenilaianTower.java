    private void prosesPenilaian() {
        btProsesPenil.setEnabled(false);
        PenilaianTower penil = new PenilaianTower(nop, thnPenilaian);
        penil.mulaiPenilaian();
        JOptionPane.showMessageDialog(this, "Proses penilaian telah selesai dilakukan ...");
        btProsesPenil.setEnabled(true);
    }
    
    private void cariNop() {
        spop = new Spop();
        ctrl.cariNop(nop, spop);
        
        if (spop.getNop()!=null && ctrl.cekBngDiLspop(nop)) {
            lbNmWp.setText(spop.getNamaWp());
            btProsesPenil.setEnabled(true);
            btCetak.setEnabled(true);
        } else {
            JOptionPane.showMessageDialog(this, "NOP tidak ditemukan atau tidak memiliki bangunan !!!");
            txtNop.setText("");
            lbNmWp.setText("");
            btProsesPenil.setEnabled(false);
            btCetak.setEnabled(false);
        }
    }
    private void isiGrid(int kode) {
        if (kode==0) {
            tblTower.setModel(ctrl.getTableModel());
        } else {
            tblTower.setModel(ctrl.getTableModel(nop));
        }
        tblTower.getColumnModel().getColumn(0).setPreferredWidth(5);        
        tblTower.getColumnModel().getColumn(1).setPreferredWidth(150);        
        tblTower.getColumnModel().getColumn(2).setPreferredWidth(50);        
        tblTower.setRowHeight(35);
    }

