    private void txtNopKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNopKeyPressed
        // TODO add your handling code here:
        if (evt.getKeyCode()==KeyEvent.VK_ENTER) {
            nop = txtNop.getText().replace(".","").replace("-","");
            cariNop();
            isiGrid(1);
        }
    }//GEN-LAST:event_txtNopKeyPressed

    private void btProsesPenilActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btProsesPenilActionPerformed
        // TODO add your handling code here:
        thnPenilaian = cbThnPenilaian.getSelectedItem().toString();
        
        try {
            if (ctrl.cekHslPenilaian(nop, thnPenilaian)) {
                if (JOptionPane.showConfirmDialog(this, "NOP "+nop+" untuk tahun "
                  +thnPenilaian+" sudah pernah dilakukan penilaian, apakah dilakukan penilaian ulang ?", 
                   "Konfirmasi Proses Penilaian", JOptionPane.YES_NO_OPTION)==JOptionPane.YES_OPTION) {
                    prosesPenilaian();
                }
            } else {
                prosesPenilaian();
            }
        } catch (Exception e) {}
    }//GEN-LAST:event_btProsesPenilActionPerformed

    private void btCetakActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCetakActionPerformed
        // TODO add your handling code here:
        ArrayList<Tower> listTower = ctrl.isiListTower(nop);
        ArrayList<NilaiTower> listNilaiTower = ctrl.isiListNilaiTower(nop, thnPenilaian);
        
        TampilkanReportTower rep = new TampilkanReportTower(spop, listTower, listNilaiTower);
        rep.tampilReport();
    }//GEN-LAST:event_btCetakActionPerformed

