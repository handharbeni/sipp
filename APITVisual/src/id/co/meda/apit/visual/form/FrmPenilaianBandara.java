    private void txtNopKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNopKeyPressed
        // TODO add your handling code here:
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            
            this.nop = txtNop.getText().replace(".", "").replace("-", "");
            
            if (control.cekNOP(nop)) {
                cbTahun.setEnabled(true);
                control.isiCbTahun(cbTahun);
                
                //set selected year-----------------///
                Date date = new Date();
                Calendar cal = Calendar.getInstance();
                cal.setTime(date);
                String year = cal.get(Calendar.YEAR)+"";
                cbTahun.setSelectedItem(year);
                //---------------------------------///
                
                
                this.labelNamaBandara.setText(control.getNamaBandara());
                btnProsesPenilBngPTDbkb.setEnabled(true);
            } else {
                JOptionPane.showMessageDialog(this, "NOP tidak ditemukan !!!");
                txtNop.setText("");
                control.isiCbTahun(cbTahun);
                cbTahun.setEnabled(false);
                btnProsesPenilBngPTDbkb.setEnabled(false);
                this.btnCetakNilai.setEnabled(false);
            }
        }
    }//GEN-LAST:event_txtNopKeyPressed

