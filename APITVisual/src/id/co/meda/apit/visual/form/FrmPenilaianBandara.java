    private void isiTableLspop(String nop, String tahun)
    {   
        this.tableLspop.setModel(control.getModelLspopBandara(nop, tahun));
        this.tableLspop.removeColumn(this.tableLspop.getColumnModel().getColumn(0));
        
        final String nop1 = nop;
        final String tahun1 = tahun;
        
        this.tableLspop.getModel().addTableModelListener(new TableModelListener(){
        
            @Override
            public void tableChanged(TableModelEvent e)
            {
                double luas = (double)tableLspop.getModel().getValueAt(tableLspop.getSelectedRow(), 2);
                double nilPerM = (double)tableLspop.getModel().getValueAt(tableLspop.getSelectedRow(), 3);
                int noBng = Integer.parseInt((String) 
                        tableLspop.getModel().getValueAt(tableLspop.getSelectedRow(), 0));
                control.nilaiLspopBandara(nop1, tahun1, noBng, nilPerM, luas);
                
                isiTableLspop(nop1,tahun1);
                totalNjop();
            }
        
        });
        
        if(!nop.equalsIgnoreCase("") && !tahun.equalsIgnoreCase(""))
        {
            DecimalFormat df2 = new DecimalFormat( "#,###,###,##0.00" );
            this.labelNjopLspopBng.setText("Rp "+df2.format(control.getTotalNjopLspop(nop, tahun)));
        }else
        {
            this.labelNjopLspopBng.setText("Rp 0.0");
        
        }
    }
    
