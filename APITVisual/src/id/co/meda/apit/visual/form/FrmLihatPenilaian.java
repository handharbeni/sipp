    private void txtNopKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNopKeyPressed
        // TODO add your handling code here:
        if (evt.getKeyCode()==KeyEvent.VK_ENTER) {
            nop = txtNop.getText().replace(".", "").replace("-","");
            if (ctrlLihPenil.cekNop(nop)) {
                isiData();
                cbThnPenilaian.requestFocus();
                btLihat.setEnabled(true);
            } else {
                JOptionPane.showMessageDialog(this, "NOP tidak ditemukan atau belum dilakukan proses penilaian !!!");
                kosongkanElemen();
            }
        }
    }//GEN-LAST:event_txtNopKeyPressed

