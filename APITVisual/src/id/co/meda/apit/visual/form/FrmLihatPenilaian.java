    private void cbBngKeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbBngKeActionPerformed
        // TODO add your handling code here:
        String noBng = (cbBngKe.getItemCount()>0)?cbBngKe.getSelectedItem().toString():"";
        
        if (!noBng.equals("Pilih Bng") && noBng.trim().length()>0) {
            for (int i=0;i<listNilBng.size();i++) {
                if (listNilBng.get(i).getBngKe().equals(noBng)) {
                    nilBng = listNilBng.get(i);
                    isiDetilBangunan();
                }
            }
        } else {kosongkanDetilBng();}
    }//GEN-LAST:event_cbBngKeActionPerformed

