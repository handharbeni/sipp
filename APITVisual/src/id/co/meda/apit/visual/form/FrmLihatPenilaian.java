    private void isiLabelSelainGrid(Integer kode, ItemPenilaian itemPenil, JLabel strNm, JLabel strLuas, JLabel strNilai, JLabel strTotal) {
        if (kode==0) {
            strNm.setText("-");
            strLuas.setText("-");
            strNilai.setText("-");
            strTotal.setText("-");
        } else {
            strNm.setText(itemPenil.getNamaItem());
            strLuas.setText(numFormat.format(itemPenil.getLuasItem())+" "+itemPenil.getSatuanItem());
            strNilai.setText("Rp. "+numFormat.format(itemPenil.getNilaiKomponen()));
            strTotal.setText("Rp. "+numFormat.format(itemPenil.getTotalNilaiItem()));
        }
    }

