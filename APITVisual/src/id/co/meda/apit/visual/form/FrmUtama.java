    private void mImpSismiopActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mImpSismiopActionPerformed
        // TODO add your handling code here:
        if (ToolsKonfigurasi.cekFileKonf()) {
            try {
                Konfigurasi konf = ToolsKonfigurasi.bacaKonf();
                DBConnectionOracle db = new DBConnectionOracle(konf.getDbSisConnStr(), konf.getDbSisUser(), konf.getDbSisPwd());
                Connection conn = db.getConnect();
                if (conn==null) {
                    JOptionPane.showMessageDialog(this, "Koneksi ke server SISMIOP gagal, cek kembali konfigurasi anda..");
                } else {
                    FrmImportSismiop frmSis = new FrmImportSismiop(this, true, conn);
                    frmSis.setVisible(true);
                }
            } catch (Exception e) {}
        } else {
            JOptionPane.showMessageDialog(this, "Koneksi ke server SISMIOP tidak dapat dilakukan,"
                    + "\nperlu dilakukan setting file konfigurasi. "
                    + "\nHubungi administrator aplikasi ...");
        }
    }//GEN-LAST:event_mImpSismiopActionPerformed

