    private void aturMenu() {
        switch (pengguna.getKdRole()) {
            case "02" :
                mTambahPengguna.setEnabled(false);
                mHubSismiop.setEnabled(false);
                mEditData.setEnabled(false);
                mProsesPenilaian.setEnabled(false);
                break;
            case "03" :
                mTambahPengguna.setEnabled(false);
                mSalinDbkb.setEnabled(false);
                mEditRef.setEnabled(false);
                mHitDbkb.setEnabled(false);
                break;
            case "04" :
                mTambahPengguna.setEnabled(false);
                mHubSismiop.setEnabled(false);
                mSalinDbkb.setEnabled(false);
                mEditRef.setEnabled(false);
                mHitDbkb.setEnabled(false);
                mEditData.setEnabled(false);
                mProsesPenilaian.setEnabled(false);
                break;
        }
    }
    
