    private void txtBngKeKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtBngKeKeyPressed
        // TODO add your handling code here:
        if (evt.getKeyCode()==KeyEvent.VK_ENTER) {
            //txtBngKe.toString().
            txtBngKe.setText(String.format("%3s",txtBngKe.getText()).replace(" ", "0").substring(0, 3));
            
            if (!txtBngKe.getText().equals("000")) {
                if (ctrl.cekNoBng(nop, txtBngKe.getText())) {
                    int reply = JOptionPane.showConfirmDialog(this, "Nomor bangunan : " + txtBngKe.getText()
                        + " sudah ada, apakah akan dilakukan pemutakhiran data ?",
                        "Konfirmasi Nomor Bangunan", JOptionPane.YES_NO_OPTION);

                    if (reply == JOptionPane.YES_OPTION) {
                        this.bngKe = txtBngKe.getText();
                        this.kdEdit = 1;
                        ambilData();
                        elementsControl(1);
                    } else {
                        txtBngKe.setText("");
                    }
                } else {
                    txtThnBangun.setEnabled(true);
                    txtThnRenov.setEnabled(true);
                    cbKondisi.setEnabled(true);
                    txtTinggi.setEnabled(true);
                    cbTipe.setEnabled(true);
                    txtThnBangun.requestFocus();
                }
            }
        }
    }//GEN-LAST:event_txtBngKeKeyPressed

    
