    private void btSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSimpanActionPerformed
        // TODO add your handling code here:
        bngKe = txtBngKe.getText();
        if (!(bngKe.equals("") || bngKe.equals("000"))) {
            if (!cekTipeTower()) {
                JOptionPane.showMessageDialog(this, "Inputan tinggi dan tipe tower tidak cocok, silahkan diperbaiki !!!");
            } else {
                tower = new Tower();
                tower.setNop(nop);
                tower.setNoBng(bngKe);
                tower.setThnBangun(txtThnBangun.getText());
                tower.setThnRenov(txtThnRenov.getText());
                tower.setKondisi(cbKondisi.getSelectedItem().toString());
                tower.setTipe(cbTipe.getSelectedItem().toString());
                tower.setTinggi(Integer.parseInt((txtTinggi.getText().equals(""))?"0":txtTinggi.getText()));
                tower.setJmlKaki(4);
                tower.setKonstruksi("baja");
                tower.setPemasangan("di atas tanah");
                String status = ctrl.simpanData(kdEdit,tower);

                if (status.equals("ok")) { 
                    JOptionPane.showMessageDialog(this, "Simpan data berhasil");
                } else {
                    JOptionPane.showMessageDialog(this, "Simpan data gagal !!!");
                }

                this.setVisible(false);
                FrmEditLspopTower frm = new FrmEditLspopTower(new JFrame(), true);
                frm.setVisible(true);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Nomor bangunan harus diisi !!!");
            txtBngKe.requestFocus();
        }
    }//GEN-LAST:event_btSimpanActionPerformed
    
    private boolean cekTipeTower() {
        Integer tinggi = Integer.parseInt((txtTinggi.getText().equals(""))?"0":txtTinggi.getText());
        String tipe = cbTipe.getSelectedItem().toString();
        return (tinggi <= 100 || !tipe.equals("SST")) && (tinggi >= 101 || !tipe.equals("Greenfield"));
        
    }
    
    private void elementsControl(Integer kode) {
        if (kode==0) {
            lbStatus.setText("Perekaman Data Baru");
            txtBngKe.setEnabled(true);
            txtThnBangun.setEnabled(false);
            txtThnRenov.setEnabled(false);
            txtTinggi.setEnabled(false);
            cbTipe.setEnabled(false);
            cbKondisi.setEnabled(false);
        } else if (kode==1) {
            lbStatus.setText("Pemutakhiran Data");
            txtBngKe.setEnabled(false);
            txtThnBangun.setEnabled(true);
            txtThnRenov.setEnabled(true);
            txtTinggi.setEnabled(true);
            cbTipe.setEnabled(true);
            cbKondisi.setEnabled(true);
        }
    }

