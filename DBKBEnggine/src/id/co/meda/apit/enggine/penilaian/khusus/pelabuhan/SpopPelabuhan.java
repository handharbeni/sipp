package id.co.meda.apit.enggine.penilaian.khusus.pelabuhan;

/**
 *
 * @author I Putu Medagia A
 */
public class SpopPelabuhan {
    
    private String nop;
    private String jenisPelabuhan;
    private String namaPelabuhan;
    private String alamatPelabuhan;

    /**
     * @return the nop
     */
    public String getNop() {
        return nop;
    }

    /**
     * @param nop the nop to set
     */
    public void setNop(String nop) {
        this.nop = nop;
    }

    /**
     * @return the jenisPelabuhan
     */
    public String getJenisPelabuhan() {
        return jenisPelabuhan;
    }

    /**
     * @param jenisPelabuhan the jenisPelabuhan to set
     */
    public void setJenisPelabuhan(String jenisPelabuhan) {
        this.jenisPelabuhan = jenisPelabuhan;
    }

    /**
     * @return the namaPelabuhan
     */
    public String getNamaPelabuhan() {
        return namaPelabuhan;
    }

    /**
     * @param namaPelabuhan the namaPelabuhan to set
     */
    public void setNamaPelabuhan(String namaPelabuhan) {
        this.namaPelabuhan = namaPelabuhan;
    }

    /**
     * @return the alamatPelabuhan
     */
    public String getAlamatPelabuhan() {
        return alamatPelabuhan;
    }

    /**
     * @param alamatPelabuhan the alamatPelabuhan to set
     */
    public void setAlamatPelabuhan(String alamatPelabuhan) {
        this.alamatPelabuhan = alamatPelabuhan;
    }
    
}
