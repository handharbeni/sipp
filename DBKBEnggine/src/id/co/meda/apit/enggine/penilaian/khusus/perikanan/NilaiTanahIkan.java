package id.co.meda.apit.enggine.penilaian.khusus.perikanan;


/**
 *
 * @author I Putu Medagia A
 */
public class NilaiTanahIkan {
    
    private String nop;
    private String tahun;
    private String kode_peruntukan;
    private double nir;
    private String kelasTanah;
    private double nilaiPerm2;
    private double nilaiKeseluruhan;

    /**
     * @return the nop
     */
    public String getNop() {
        return nop;
    }

    /**
     * @param nop the nop to set
     */
    public void setNop(String nop) {
        this.nop = nop;
    }

    /**
     * @return the tahun
     */
    public String getTahun() {
        return tahun;
    }

    /**
     * @param tahun the tahun to set
     */
    public void setTahun(String tahun) {
        this.tahun = tahun;
    }

    /**
     * @return the kode_peruntukan
     */
    public String getKode_peruntukan() {
        return kode_peruntukan;
    }

    /**
     * @param kode_peruntukan the kode_peruntukan to set
     */
    public void setKode_peruntukan(String kode_peruntukan) {
        this.kode_peruntukan = kode_peruntukan;
    }

    /**
     * @return the nir
     */
    public double getNir() {
        return nir;
    }

    /**
     * @param nir the nir to set
     */
    public void setNir(double nir) {
        this.nir = nir;
    }

    /**
     * @return the kelasTanah
     */
    public String getKelasTanah() {
        return kelasTanah;
    }

    /**
     * @param kelasTanah the kelasTanah to set
     */
    public void setKelasTanah(String kelasTanah) {
        this.kelasTanah = kelasTanah;
    }

    /**
     * @return the nilaiPerm2
     */
    public double getNilaiPerm2() {
        return nilaiPerm2;
    }

    /**
     * @param nilaiPerm2 the nilaiPerm2 to set
     */
    public void setNilaiPerm2(double nilaiPerm2) {
        this.nilaiPerm2 = nilaiPerm2;
    }

    /**
     * @return the nilaiKeseluruhan
     */
    public double getNilaiKeseluruhan() {
        return nilaiKeseluruhan;
    }

    /**
     * @param nilaiKeseluruhan the nilaiKeseluruhan to set
     */
    public void setNilaiKeseluruhan(double nilaiKeseluruhan) {
        this.nilaiKeseluruhan = nilaiKeseluruhan;
    }
    
  
}
