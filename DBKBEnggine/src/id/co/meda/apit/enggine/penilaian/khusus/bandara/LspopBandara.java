public class LspopBandara {
    
    private int no_bng;
    private String nop;
    private String jpb;
    private double luas;

    /**
     * @return the id
     */
    public int getNoBng() {
        return no_bng;
    }

    /**
     * @param id the id to set
     */
    public void setNoBng(int noBng) {
        this.no_bng = noBng;
    }

    /**
     * @return the nop
     */
    public String getNop() {
        return nop;
    }

    /**
     * @param nop the nop to set
     */
    public void setNop(String nop) {
        this.nop = nop;
    }

    /**
     * @return the jpb
     */
    public String getJpb() {
        return jpb;
    }

    /**
     * @param jpb the jpb to set
     */
    public void setJpb(String jpb) {
        this.jpb = jpb;
    }

    /**
     * @return the luas
     */
    public double getLuas() {
        return luas;
    }

    /**
     * @param luas the luas to set
     */
    public void setLuas(double luas) {
        this.luas = luas;
    }
    
    
}
