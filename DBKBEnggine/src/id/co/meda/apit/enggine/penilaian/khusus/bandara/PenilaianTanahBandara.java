package id.co.meda.apit.enggine.penilaian.khusus.bandara;

/**
 *
 * @author I Putu Medagia A
 */
public class PenilaianTanahBandara {
    
    private String jnsPenggunaanTanah = "";
    private double luas = 0;
    private double nir = 0;
    private double nilPerM = 0;
    private double nilTotal = 0;

    /**
     * @return the jnsPenggunaanTanah
     */
    public String getJnsPenggunaanTanah() {
        return jnsPenggunaanTanah;
    }

    /**
     * @param jnsPenggunaanTanah the jnsPenggunaanTanah to set
     */
    public void setJnsPenggunaanTanah(String jnsPenggunaanTanah) {
        this.jnsPenggunaanTanah = jnsPenggunaanTanah;
    }

    /**
     * @return the luas
     */
    public double getLuas() {
        return luas;
    }

    /**
     * @param luas the luas to set
     */
    public void setLuas(double luas) {
        this.luas = luas;
    }

    /**
     * @return the nir
     */
    public double getNir() {
        return nir;
    }

    /**
     * @param nir the nir to set
     */
    public void setNir(double nir) {
        this.nir = nir;
    }

    /**
     * @return the nilPerM
     */
    public double getNilPerM() {
        return nilPerM;
    }

    /**
     * @param nilPerM the nilPerM to set
     */
    public void setNilPerM(double nilPerM) {
        this.nilPerM = nilPerM;
    }

    /**
     * @return the nilTotal
     */
    public double getNilTotal() {
        return nilTotal;
    }

    /**
     * @param nilTotal the nilTotal to set
     */
    public void setNilTotal(double nilTotal) {
        this.nilTotal = nilTotal;
    }
    
    
    
    
}
