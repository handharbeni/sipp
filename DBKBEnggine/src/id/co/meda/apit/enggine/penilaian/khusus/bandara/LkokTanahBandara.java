package id.co.meda.apit.enggine.penilaian.khusus.bandara;
/**
 *
 * @author I Putu Medagia A
 */
public class LkokTanahBandara {
    
    private String nop;
    private String namaBandara;
    private String alamat;
    private double luasKomersial;
    private double luasNonKomersial;
    private double luasCadangan;
    private double luasArealLain;

    /**
     * @return the nop
     */
    public String getNop() {
        return nop;
    }

    /**
     * @param nop the nop to set
     */
    public void setNop(String nop) {
        this.nop = nop;
    }

    /**
     * @return the namaBandara
     */
    public String getNamaBandara() {
        return namaBandara;
    }

    /**
     * @param namaBandara the namaBandara to set
     */
    public void setNamaBandara(String namaBandara) {
        this.namaBandara = namaBandara;
    }

    /**
     * @return the alamat
     */
    public String getAlamat() {
        return alamat;
    }

    /**
     * @param alamat the alamat to set
     */
    public void setAlamat(String alamat) {
        this.alamat = alamat;
    }

    /**
     * @return the luasKomersial
     */
    public double getLuasKomersial() {
        return luasKomersial;
    }

    /**
     * @param luasKomersial the luasKomersial to set
     */
    public void setLuasKomersial(double luasKomersial) {
        this.luasKomersial = luasKomersial;
    }

    /**
     * @return the luasNonKomersial
     */
    public double getLuasNonKomersial() {
        return luasNonKomersial;
    }

    /**
     * @param luasNonKomersial the luasNonKomersial to set
     */
    public void setLuasNonKomersial(double luasNonKomersial) {
        this.luasNonKomersial = luasNonKomersial;
    }

    /**
     * @return the luasCadangan
     */
    public double getLuasCadangan() {
        return luasCadangan;
    }

    /**
     * @param luasCadangan the luasCadangan to set
     */
    public void setLuasCadangan(double luasCadangan) {
        this.luasCadangan = luasCadangan;
    }

    /**
     * @return the luasArealLain
     */
    public double getLuasArealLain() {
        return luasArealLain;
    }

    /**
     * @param luasArealLain the luasArealLain to set
     */
    public void setLuasArealLain(double luasArealLain) {
        this.luasArealLain = luasArealLain;
    }
    
}
