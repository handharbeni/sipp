package id.co.meda.apit.enggine.penilaian.khusus.bandara;

/**
 *
 * @author I Putu Medagia A
 */
public class PenilaianLspopBandara {
    
    private LspopBandara lspop;
    private double nilaiPerM;
    private double nilaiTotal;

    /**
     * @return the lspop
     */
    public LspopBandara getLspop() {
        return lspop;
    }

    /**
     * @param lspop the lspop to set
     */
    public void setLspop(LspopBandara lspop) {
        this.lspop = lspop;
    }

    /**
     * @return the nilaiPerM
     */
    public double getNilaiPerM() {
        return nilaiPerM;
    }

    /**
     * @param nilaiPerM the nilaiPerM to set
     */
    public void setNilaiPerM(double nilaiPerM) {
        this.nilaiPerM = nilaiPerM;
    }

    /**
     * @return the nilaiTotal
     */
    public double getNilaiTotal() {
        return nilaiTotal;
    }

    /**
     * @param nilaiTotal the nilaiTotal to set
     */
    public void setNilaiTotal(double nilaiTotal) {
        this.nilaiTotal = nilaiTotal;
    }
    
}
